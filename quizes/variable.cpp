# include <iostream>

using namespace std;

int main () {
    float width = 4.5;
    float height = 5.5;

    int area = width * height;
    cout << width * height << endl;
    cout << "area = " << area << endl;

    int numerator = 4;
    int denominator = 5;

    int answer = numerator / denominator;
    cout << numerator / denominator << endl;
    cout << "answer = " << answer << endl;
    
    return 0;
}
